#include <string>
////////////////////////////////////////////////////////////////////////////////////////////////
namespace Saadin {
   struct Employee;
   class Node;
   class Doubly;
}
////////////////////////////////////////////////////////////////////////////////////////////////
struct Saadin::Employee {
   std::string Name;
   int ID;
};
////////////////////////////////////////////////////////////////////////////////////////////////
class Saadin::Node {
private:
   Saadin::Employee m_Data;
   Saadin::Node* m_Next;
   Saadin::Node* m_Previous;

public:
   Node();
   Node(const Saadin::Employee&);
   Node(const Saadin::Employee&, Saadin::Node*);
   Node(const Saadin::Employee&, Saadin::Node*, Saadin::Node*);

   void SetData(const Saadin::Employee&);
   void SetNext(Saadin::Node*);
   void SetPrevious(Saadin::Node*);

   Saadin::Employee GetData() const;
   Saadin::Node* GetNext() const;
   Saadin::Node* GetPrevious() const;
};
////////////////////////////////////////////////////////////////////////////////////////////////
Saadin::Node::Node() {
   this->m_Data = { "Not Specified Yet",  -1 };
   this->m_Next = NULL;
   this->m_Previous = NULL;
}
////////////////////////////////////////////////////////////////////////////////////////////////
Saadin::Node::Node(const Saadin::Employee& Data) {
   this->m_Data = Data;
   this->m_Next = NULL;
   this->m_Previous = NULL;
}
////////////////////////////////////////////////////////////////////////////////////////////////
Saadin::Node::Node(const Saadin::Employee& Data, Saadin::Node* Next) {
   this->m_Data = Data;
   this->m_Next = Next;
   this->m_Previous = NULL;
}
////////////////////////////////////////////////////////////////////////////////////////////////
Saadin::Node::Node(const Saadin::Employee& Data, Saadin::Node* Next, Saadin::Node* Previous) {
   this->m_Data = Data;
   this->m_Next = Next;
   this->m_Previous = Previous;
}
////////////////////////////////////////////////////////////////////////////////////////////////
void Saadin::Node::SetData(const Saadin::Employee& Data) {
   this->m_Data = Data;
}
////////////////////////////////////////////////////////////////////////////////////////////////
void Saadin::Node::SetNext(Saadin::Node* Next) {
   this->m_Next = Next;
}
////////////////////////////////////////////////////////////////////////////////////////////////
void Saadin::Node::SetPrevious(Saadin::Node* Previous) {
   this->m_Previous = Previous;
}
////////////////////////////////////////////////////////////////////////////////////////////////
Saadin::Employee Saadin::Node::GetData() const {
   return this->m_Data;
}
////////////////////////////////////////////////////////////////////////////////////////////////
Saadin::Node* Saadin::Node::GetNext() const {
   return this->m_Next;
}
////////////////////////////////////////////////////////////////////////////////////////////////
Saadin::Node* Saadin::Node::GetPrevious() const {
   return this->m_Previous;
}
